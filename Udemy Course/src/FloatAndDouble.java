public class FloatAndDouble {
    public static void main(String[] args) {
        float maxfloat=Float.MAX_VALUE;
        float Minfloat=Float.MIN_VALUE;

        System.out.println("Float min is: " + Minfloat);
        System.out.println("Float max is: " + maxfloat);

        double MaxDouble = Double.MAX_VALUE;
        double MinDouble = Double.MIN_VALUE;

        System.out.println("Float min is: " + MinDouble);
        System.out.println("Float max is: " + MaxDouble);

        int intValue = 5 / 2;
        float floatvalue= 5f / 2f;
        double doublevalue = 5d / 2d;
        System.out.println("Int Value: " + intValue);
        System.out.println("Float Value: " + floatvalue);
        System.out.println("Double Value: " + doublevalue);
   
    }
    
    

}

